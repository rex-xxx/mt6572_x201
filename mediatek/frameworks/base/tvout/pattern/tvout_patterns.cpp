bool TVOutPatterns::unZipPattern(unsigned char *in, unsigned char *out, int inlen, int outlen)
{
    int ret;
    unsigned have;
    z_stream strm;

    memset(&strm, 0, sizeof(z_stream));
    /* allocate inflate state */
    strm.zalloc = Z_NULL;
    strm.zfree = Z_NULL;
    strm.opaque = Z_NULL;
    strm.avail_in = 0;
    strm.next_in = Z_NULL;
    ret = inflateInit(&strm);
    if (ret != Z_OK)
        return ret;

    /* decompress until deflate stream ends or end of file */
    do {
        strm.avail_in = inlen;
        if (strm.avail_in == 0)
            break;
        strm.next_in = in;

        /* run inflate() on input until output buffer not full */
        do {
            strm.avail_out = outlen;
            strm.next_out = out;
            ret = inflate(&strm, Z_NO_FLUSH);
            switch (ret) {
            case Z_NEED_DICT:
                ret = Z_DATA_ERROR;     /* and fall through */
            case Z_DATA_ERROR:
            case Z_MEM_ERROR:
                (void)inflateEnd(&strm);
                return ret;
            }
            have = outlen - strm.avail_out;
        } while (strm.avail_out == 0);

        /* done when inflate() says it's done */
    } while (ret != Z_STREAM_END);

    /* clean up and return */
    (void)inflateEnd(&strm);
    return ret == Z_STREAM_END ? Z_OK : Z_DATA_ERROR;
}

void TVOutPatterns::getPatternInfo(int ID, TVOutPatInfo* Info)
{
    unsigned int pixelCnt = TVOUT_PATTERN_WIDTH * TVOUT_PATTERN_HEIGHT ;
    Info->w = TVOUT_PATTERN_WIDTH;
    Info->h = TVOUT_PATTERN_HEIGHT;


    switch (ID)
    {
        // YUV
        case 0:
        case 4:
        case 5:
        case 6:
            Info->size = pixelCnt * 3 / 2;
            Info->fmt = TVOUT_FMT_YUV420_PLANAR;
            break;

        // RGB565
        case 1:
        case 2:
        case 3:
            Info->size = pixelCnt * 2;
            Info->fmt = TVOUT_FMT_RGB565;
            break;

        default:
            XLOGE("ERROR: input wrong pattern ID");
            break;
    }
    return;

}


