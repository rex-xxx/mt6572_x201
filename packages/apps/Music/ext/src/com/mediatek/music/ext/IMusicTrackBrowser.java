package com.mediatek.music.ext;

public interface IMusicTrackBrowser {

    /**
     * enable the clear playlist menu item when trackbrower show playlist item. Returns whether the
     * clear playlist menu item is enable.
     */
    public boolean enableClearPlaylistMenu();

    /**
     * enable the add folder to play menu item when in song tab.
     */
    public boolean enableAddFolderToPlayMenu();

    /**
     * enable the add folder as playlist menu item when in song tab.
     */
    public boolean enableAddFolderAsPlaylistMenu();

    /**
     * enable the add song to play menu item when in playlist tab.
     */
    public boolean enableAddSongToPlayMenu();

    /**
     * enable the add song menu item when in playlist tab.
     */
    public boolean enableAddSongMenu();

    /**
     * get the string for add a non audio file. Returns the string for add a non audio file.
     */
    public String getNoneAudioString();

    /**
     * get the string for selecting a empty folder.
     */
    public String getEmptyFolderString();
}
